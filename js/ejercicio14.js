/**4. Conversión de temperaturas: Crea un programa que convierta grados Celsius
a Fahrenheit. */


// Solicitar al usuario que introduzca la temperatura en Celsius
let celsius = parseFloat(prompt("Introduce la temperatura en grados Celsius:"));
convercion=(celsius * 9/5) + 32;
let fahrenheit = convercion;
console.log(`${celsius}°C es igual a ${fahrenheit}°F`);



